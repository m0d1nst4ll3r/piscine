#include <unistd.h>

// -----------------------------------------------------------------------
//  ft_putchar :
//
//  Prints c on standard output.
//
//  - c:	char to print
// -----------------------------------------------------------------------
void	ft_putchar(char c)
{
	write(1, &c, 1);
}
