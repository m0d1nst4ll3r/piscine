unsigned int	ft_strlen(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	len;

	i = 0;
	len = ft_strlen(src);
	while (i < size && i <= len)
	{
		if (i == size - 1)
			dest[i] = 0;
		else
			dest[i] = src[i];
		i++;
	}
	return (len);
}
